CREATE TABLE MANAGE_LOAN
( ID                                NUMBER(19) GENERATED BY DEFAULT AS IDENTITY
, UUID                              VARCHAR2(36 CHAR)         NOT NULL
, DETAILS                           VARCHAR2(255 CHAR)
, PERSON_ID                         NUMBER(19)                NOT NULL
, REGISTERED_TIMESTAMP              TIMESTAMP                 NOT NULL
, VALIDATED_TIMESTAMP               TIMESTAMP
, ENRICHED_TIMESTAMP                TIMESTAMP
, PENDING_TIMESTAMP                 TIMESTAMP
, AUGMENTED_TIMESTAMP               TIMESTAMP
, ENTITLED_TIMESTAMP                TIMESTAMP
, REJECTED_TIMESTAMP                TIMESTAMP
, CONSTRAINT MANAGE_LOAN_PK         PRIMARY KEY (ID)
, CONSTRAINT MANAGE_LOAN_PERSON_FK  FOREIGN KEY (PERSON_ID) REFERENCES PERSON(ID)
, CONSTRAINT MANAGE_LOAN_UUID_UC    UNIQUE (UUID)
);